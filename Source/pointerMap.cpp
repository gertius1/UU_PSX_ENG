int getBlockIndexFromNPCnr(int tablePosition)
{
	if (tablePosition < nrOfNPCs)
		return pointerMap[tablePosition][0];
	else
		return -1;
}

int getNPCnrFromBlockIndex(int blockIndex)
{
	for (int i = 0; i < nrOfNPCs; i++)
	{
		if (pointerMap[i][0] == blockIndex)
			return i;
	}
	return -1;
}